let currentLocale: Locale = DEFAULT_LOCALE;

export const setLocale = (locale: Locale) => {
  currentLocale = locale;
};

export const getLocale = (): Locale => currentLocale;

export const getLocaleTag = (): string => localeTags[currentLocale] || localeTags[DEFAULT_LOCALE];

export const translate = (key: string, params?: Record<string, string | number>) => {
  const dict = translations[currentLocale] || translations[DEFAULT_LOCALE];
  let value = dict[key] || translations[DEFAULT_LOCALE][key] || key;
  if (params) {
    Object.keys(params).forEach((paramKey) => {
      value = value.replace(new RegExp(`{{${paramKey}}}`, "g"), String(params[paramKey]));
    });
  }
  return value;
};
